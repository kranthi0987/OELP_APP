
package mahiti.org.oelp.videoplay.tracker;

public class MediaTracker {

    private String mediaId;
    private String startTime;
    private String endTime;
    private Integer comStatus;
    private String watchMin;
    private String mediaName;

    public String getMediaName() {
        return mediaName;
    }

    public void setMediaName(String mediaName) {
        this.mediaName = mediaName;
    }

    public String getMediaId() {
        return mediaId;
    }

    public void setMediaId(String mediaId) {
        this.mediaId = mediaId;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }


    public String getEndTime() {
        return endTime;
    }


    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }


    public Integer getComStatus() {
        return comStatus;
    }


    public void setComStatus(Integer comStatus) {
        this.comStatus = comStatus;
    }


    public String getWatchMin() {
        return watchMin;
    }


    public void setWatchMin(String watchMin) {
        this.watchMin = watchMin;
    }






}
